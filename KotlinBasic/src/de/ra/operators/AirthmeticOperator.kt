package de.ra.operators

fun main() {
    val firstNumber = 100
    val secondNumber = 50

    // add
    println(firstNumber + secondNumber)
    println(firstNumber.plus(secondNumber))

    // sub
    println(firstNumber - secondNumber)
    println(firstNumber.minus(secondNumber))

    // mul
    println(firstNumber * secondNumber)
    println(firstNumber.times(secondNumber))

    // div
    println(firstNumber / secondNumber)
    println(firstNumber.div(secondNumber))

    // mod
    println(firstNumber % secondNumber)
    println(firstNumber.rem(secondNumber))
}