package de.ra.operators

fun main() {
    var num: Int

    num = 100       // Assign 100 to num
    num += 20       // Adds and then assign to num
    num -= 20       // Sub and then assign to num
    num *= 100      // Mul and then assign to num
    num /= 100      // Div and then assign to num
    num %= 98       // Mod and then assign to num
}