package de.ra.operators

fun main() {
    val firstNumber = 500
    val secondNumber = 50

    println(firstNumber > secondNumber)     // Greater than
    println(firstNumber >= secondNumber)    // Greater than or equal
    println(firstNumber < secondNumber)     // Less than
    println(firstNumber <= secondNumber)    // Less than or equal
    println(firstNumber == secondNumber)    // Equal to
    println(firstNumber != secondNumber)    // Not equal to
}