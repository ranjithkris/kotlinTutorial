package de.ra.datatype

fun main() {
    // For num1 variable a integer value is assigned, therefore num1 is of type kotlin.Int
    val num1 = 1
    println(num1)

    // We can also specify the type of a variable explicitly,
    // Example in the below statement, num2 is defined as Int separated with the ":"
    val num2: Int = 2
    println(num2)

    // Another example
    var var1 = 34.56
    var var2 = 15.44
    var sumResult: Double

    sumResult = var1 + var2

    println(sumResult)

    /**
     * Below are some data types.
     *
     * 1. Numbers
     * 2. Booleans
     * 3. Characters
     * 4. String
     * 5. Array
     */
}